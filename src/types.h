/// Types d'entiers
typedef __INTPTR_TYPE__  zint;   // Type entier "machine" dans Z (signé)
typedef __UINTPTR_TYPE__ nint;   // Type entier "machine" dans N (non signé)
typedef __INT8_TYPE__    zint8;  // Type entier sur 8 bits dans Z
typedef __UINT8_TYPE__   nint8;  // Type entier sur 8 bits dans N
typedef __INT16_TYPE__   zint16; // Type entier sur 16 bits dans Z
typedef __UINT16_TYPE__  nint16; // Type entier sur 16 bits dans N
typedef __INT32_TYPE__   zint32; // Type entier sur 32 bits dans Z
typedef __UINT32_TYPE__  nint32; // Type entier sur 32 bits dans N
typedef __INT64_TYPE__   zint64; // Type entier sur 64 bits dans Z
typedef __UINT64_TYPE__  nint64; // Type entier sur 64 bits dans N

/// Formats pour les entiers "machine"
#if defined CONFIG_64BIT
#define NINT "%lu"
#define ZINT "%ld"
#else
#define NINT "%u"
#define ZINT "%d"
#endif

