/** Supprime une connexion dans netfilter.
 * @param conn Connexion à supprimer
**/
static inline void hooks_interface_deleteconnection(struct nf_conn* conn) {
    nf_conntrack_destroy((struct nf_conntrack*) conn); // Suppression de la connexion
}

